<p>If requested, GreenCoinX Inc can use its software development expertise by providing software support to governments and E-commerce portals to collect taxes or fees and to build API's and mobile apps on multiple platforms. GreenCoinX is flexible and modifiable such that the government of each country can decide what identification rules they require for a GreenCoinX transaction and what country specific taxes should be attached to each transaction.  Additional parameters for further identification can be added on an as needed basis depending on the requirements of each country. GreenCoinX intends to commence negotiations with governmental authorities to establish the taxation and identification parameters that each country requires.</p>
