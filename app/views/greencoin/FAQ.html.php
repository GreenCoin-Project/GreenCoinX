<blockquote>
<p><strong>Q. Why is XGC mining done?</strong></p>
<p>
A. Mining is done to keep the XGC blockchain functioning and processing XGC transactions. When the XGC mining software accepts a transaction it is then broadcast and recorded on the XGC blockchain. When another miner accepts the transaction, that miner is rewarded with a voluntary transaction fee which is usually not less than 0.0001XGC. Currently, standard computers can be used to mine XGC, however the reward for mining reduces over time and so as more XGC miners become active and the difficulty of solving the algorithmic equations increases, it is anticipated that specially designed computers are likely to be required to complete XGC mining transactions.
</p>
</blockquote>
<blockquote>
<p><strong>Q. Why is the XGC identification feature important?</strong></p>
<p>
A. Other crypto currencies are not identifiable and therefore those crypto currencies are not only susceptible to be used for illegal purposes, but are not easily taxable by governments. These concerns make global acceptance of crypto currency transactions more difficult. GreenCoinX provides a solution by adding email and phone identification to all GreenCoinX transactions. Those intending illegal activities will likely not use GreenCoinX as they can be easily identified. Furthermore, global governments will be able to collect taxes based on GreenCoinX transactions with country by country rules for each type of transaction.</p>
</blockquote>

<blockquote>
<p><strong>Q. How does the identification process work?</strong></p>
<p>
A. GreenCoinX identification is initially done when GreenCoinX software is downloaded for storing, generating or mining, and sending or receiving XGC. The user email and phone address are verified and stored on the blockchain of GreenCoinX as a transaction with zero value. The user has the option of sending or receiving GreenCoinX to a phone, email or GreenCoinX address. All websites transacting in XGC will have a verified GreenCoinX software client installed. After installation of the software, the user is required to be identified by using the Settings-->Options-->Identification Tab. The user will have to provide email and phone information, which will be validated by a simple code sent to the user and then verified. Alternatively, the user can use Remote Procedure Call ("RPC") to get identified (RPC is a protocol that causes a procedure to execute in another address space).   </p>
</blockquote>

